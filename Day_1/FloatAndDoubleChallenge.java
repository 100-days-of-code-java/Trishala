public class FloatAndDoubleChallenge {
    public static void main(String[] args){
        //Convert a given no. of pounds to kilograms.

        double weightInPounds = 5;
        // 1 pound = 0.45359237 kgs
        double weightInKilograms = 0.45359237 * weightInPounds;
        System.out.println("Weight in Kilograms is: " + weightInKilograms);

    }
    
}
